        "\n"
        "\n"
        "def run_ml_models(df, feature_cols, cat_feature_cols, folds, test_idx, label_prefix=''):\n"
        '    """Train LightGBM + XGBoost trên CV folds và test set.\n'
        "\n"
        "    Returns\n"
        "    -------\n"
        '    results, lgb_model, xgb_model, test_preds\n'
        '    """\n'
        "    results = []\n"
        "    lgb_model_final = None\n"
        "    xgb_model_final = None\n"
        "    test_preds = {}\n"
        "\n"
        "    # LightGBM params\n"
        "    lgb_params = {\n"
