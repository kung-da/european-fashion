        "\n"
        "    # XGBoost params\n"
        "    xgb_params = {\n"
        "        'objective': 'reg:squarederror',\n"
        "        'eval_metric': 'mae',\n"
        "        'max_depth': 6,\n"
        "        'learning_rate': 0.05,\n"
        "        'n_estimators': 200,\n"
        "        'subsample': 0.8,\n"
        "        'colsample_bytree': 0.8,\n"
        "        'random_state': 42,\n"
        "        'verbosity': 0,\n"
        "        'n_jobs': -1,\n"
        "    }\n"
        "\n"
        "    # --- CV Folds ---\n"
        "    for fold_i, (train_idx, val_idx) in enumerate(folds):\n"
        "        X_tr = df.loc[train_idx, feature_cols].values\n"
        "        y_tr = df.loc[train_idx, TARGET].values\n"
        "        X_va = df.loc[val_idx, feature_cols].values\n"
