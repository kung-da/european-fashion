    # ── 8. TRAIN/TEST SPLIT ───────────────────────────────────────────────
    md(
        "## 8. Chia Train/Test — Time-based Split\n"
        "\n"
        "### Vì sao KHÔNG dùng Random Split?\n"
        "- **Data leakage**: Random split cho phép model \"nhìn thấy tương lai\" → đánh giá quá lạc quan\n"
        "- **Lag features bị corrupt**: lag_1 của ngày test có thể lấy từ ngày train đã thấy\n"
        "- **Auto-correlation**: các ngày liền kề tương quan cao → random split vi phạm giả định independence\n"
        "- **Business logic**: trong thực tế ta chỉ dự báo tương lai từ quá khứ\n"
        "\n"
        "### Phương pháp: Expanding Window\n"
        "```\n"
        "Fold 1: Train [───────] Val [──]\n"
        "Fold 2: Train [──────────] Val [──]\n"
        "Fold 3: Train [─────────────] Val [──]\n"
        "Final:  Train [────────────────] Test [──]  (7 ngày cuối)\n"
        "```\n"
        "Cutoff ngày áp dụng **đồng nhất** cho tất cả series."
    )

    code(
        "# ── Thiết lập parameters ──\n"
        "HORIZON = 7        # dự báo 7 ngày\n"
        "N_CV_FOLDS = 4     # số fold cross-validation\n"
        "\n"
        "# Feature columns (chung cho cả 2 granularity)\n"
        "TIME_FEATURES = ['day_of_week', 'day_of_month', 'week_of_year', 'month',\n"
        "                 'is_weekend', 'is_month_start', 'is_month_end']\n"
        "LAG_FEATURES = ['lag_1', 'lag_7']\n"
        "ROLLING_FEATURES = ['rolling_mean_7', 'rolling_std_7']\n"
        "PROMO_FEATURES = ['has_discount', 'has_campaign']\n"
        "\n"
        "NUMERIC_FEATURES = TIME_FEATURES + LAG_FEATURES + ROLLING_FEATURES + PROMO_FEATURES\n"
        "TARGET = 'daily_quantity'\n"
        "\n"
        "print(f'Horizon: {HORIZON} ngày')\n"
        "print(f'CV Folds: {N_CV_FOLDS}')\n"
        "print(f'Numeric features ({len(NUMERIC_FEATURES)}): {NUMERIC_FEATURES}')"
    )

    code(
        "def make_time_splits(df, date_col, horizon, n_folds):\n"
        '    """Tạo expanding-window time-based CV folds + final test split.\n'
        "\n"
        "    Returns\n"
        "    -------\n"
        "    folds : list of (train_idx, val_idx)\n"
        "    test_idx : index for final hold-out test set\n"
        '    """\n'
        "    sorted_dates = sorted(df[date_col].unique())\n"
        "    n_dates = len(sorted_dates)\n"
        "\n"
        "    # Final test: last `horizon` dates\n"
        "    test_start_date = sorted_dates[-horizon]\n"
        "    test_idx = df[df[date_col] >= test_start_date].index\n"
        "\n"
        "    # CV folds: carve from the remaining dates\n"
        "    cv_dates = sorted_dates[:-horizon]\n"
        "    n_cv = len(cv_dates)\n"
        "\n"
        "    folds = []\n"
        "    for k in range(n_folds):\n"
        "        # Expanding window: each fold adds ~(n_cv - min_train) / n_folds days\n"
        "        min_train = max(14, n_cv // 3)  # at least 14 days for training\n"
        "        val_size = horizon\n"
        "        val_end_idx = min_train + (k + 1) * ((n_cv - min_train) // n_folds) + val_size - 1\n"
        "        if val_end_idx >= n_cv:\n"
        "            val_end_idx = n_cv - 1\n"
        "        val_start_idx = val_end_idx - val_size + 1\n"
        "\n"
        "        train_end_date = cv_dates[val_start_idx - 1]\n"
        "        val_start_date = cv_dates[val_start_idx]\n"
        "        val_end_date = cv_dates[min(val_end_idx, n_cv - 1)]\n"
        "\n"
        "        train_mask = df[date_col] <= train_end_date\n"
        "        val_mask = (df[date_col] >= val_start_date) & (df[date_col] <= val_end_date)\n"
        "\n"
        "        if train_mask.sum() > 0 and val_mask.sum() > 0:\n"
        "            folds.append((df[train_mask].index, df[val_mask].index))\n"
        "\n"
        "    return folds, test_idx\n"
        "\n"
        "\n"
        "# Tạo splits cho Category level\n"
        "cat_folds, cat_test_idx = make_time_splits(\n"
        "    cat_features, 'full_date', HORIZON, N_CV_FOLDS\n"
        ")\n"
        "print(f'Category level: {len(cat_folds)} CV folds')\n"
        "for i, (tr, va) in enumerate(cat_folds):\n"
        "    print(f'  Fold {i+1}: train={len(tr)} rows, val={len(va)} rows, '\n"
        "          f'train dates up to {cat_features.loc[tr, \"full_date\"].max().date()}, '\n"
        "          f'val from {cat_features.loc[va, \"full_date\"].min().date()}')\n"
        "print(f'  Test: {len(cat_test_idx)} rows '\n"
        "      f'({cat_features.loc[cat_test_idx, \"full_date\"].min().date()} → '\n"
        "      f'{cat_features.loc[cat_test_idx, \"full_date\"].max().date()})')\n"
        "\n"
        "# Tạo splits cho Category×Country level\n"
        "catco_folds, catco_test_idx = make_time_splits(\n"
        "    catco_features, 'full_date', HORIZON, N_CV_FOLDS\n"
        ")\n"
        "print(f'\\nCategory×Country level: {len(catco_folds)} CV folds')\n"
        "for i, (tr, va) in enumerate(catco_folds):\n"
        "    print(f'  Fold {i+1}: train={len(tr)} rows, val={len(va)} rows')\n"
        "print(f'  Test: {len(catco_test_idx)} rows')"
    )

    # ── 9. METRICS ────────────────────────────────────────────────────────
