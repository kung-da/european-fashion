    code(
        "def create_features(df, group_cols):\n"
        '    """Tạo time-series features cho demand forecasting.\n'
        "\n"
        "    Parameters\n"
        "    ----------\n"
        "    df : DataFrame cần có cột 'full_date' và 'daily_quantity'\n"
        "    group_cols : list tên cột nhóm (ví dụ ['category'] hoặc ['category', 'country'])\n"
        "\n"
        "    Returns\n"
        "    -------\n"
        '    DataFrame với features mới, đã drop NaN do lag.\n'
        '    """\n'
        "    df = df.sort_values(group_cols + ['full_date']).copy()\n"
        "\n"
        "    # ── Time features ──\n"
        "    df['day_of_week'] = df['full_date'].dt.dayofweek          # 0=Mon, 6=Sun\n"
        "    df['day_of_month'] = df['full_date'].dt.day\n"
        "    df['week_of_year'] = df['full_date'].dt.isocalendar().week.astype(int)\n"
        "    df['month'] = df['full_date'].dt.month\n"
        "    df['is_weekend'] = (df['day_of_week'] >= 5).astype(int)\n"
        "    df['is_month_start'] = df['full_date'].dt.is_month_start.astype(int)\n"
        "    df['is_month_end'] = df['full_date'].dt.is_month_end.astype(int)\n"
        "\n"
        "    # ── Lag features (per group) ──\n"
        "    # lag_1: demand hôm qua → auto-correlation ngắn hạn\n"
        "    # lag_7: demand cùng ngày tuần trước → weekly pattern\n"
        "    for lag in [1, 7]:\n"
        "        df[f'lag_{lag}'] = df.groupby(group_cols)['daily_quantity'].shift(lag)\n"
        "\n"
        "    # ── Rolling features (shift 1 trước, rồi rolling → tránh leakage) ──\n"
        "    # rolling_mean_7: xu hướng trung bình 7 ngày gần nhất (smoothed)\n"
        "    # rolling_std_7 : độ biến động 7 ngày → demand ổn định hay biến động?\n"
        "    shifted = df.groupby(group_cols)['daily_quantity'].shift(1)\n"
        "    df['rolling_mean_7'] = (\n"
        "        shifted.groupby(df[group_cols].apply(tuple, axis=1))\n"
        "        .transform(lambda x: x.rolling(7, min_periods=1).mean())\n"
        "    )\n"
        "    df['rolling_std_7'] = (\n"
        "        shifted.groupby(df[group_cols].apply(tuple, axis=1))\n"
        "        .transform(lambda x: x.rolling(7, min_periods=1).std())\n"
        "    ).fillna(0)\n"
        "\n"
        "    # ── Promotion features ──\n"
        "    # has_discount: ngày có sản phẩm giảm giá không\n"
        "    # has_campaign: ngày có campaign marketing không\n"
        "    # (đã có sẵn trong dataset từ mart view)\n"
        "\n"
        "    # Drop rows thiếu lag features (7 ngày đầu)\n"
        "    df = df.dropna(subset=['lag_1', 'lag_7']).reset_index(drop=True)\n"
        "\n"
        "    return df\n"
        "\n"
        "\n"
        "print('Function create_features() defined.')"
    )

