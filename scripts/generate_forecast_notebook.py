        "        RMSE_mean=('RMSE', 'mean'), RMSE_std=('RMSE', 'std'),\n"
        "        SMAPE_mean=('SMAPE', 'mean'), SMAPE_std=('SMAPE', 'std'),\n"
        "    ).reset_index()\n"
        "    cv['CV_MAE'] = cv.apply(lambda r: f\"{r['MAE_mean']:.2f} ± {r['MAE_std']:.2f}\", axis=1)\n"
        "    cv['CV_RMSE'] = cv.apply(lambda r: f\"{r['RMSE_mean']:.2f} ± {r['RMSE_std']:.2f}\", axis=1)\n"
        "    cv['CV_SMAPE'] = cv.apply(lambda r: f\"{r['SMAPE_mean']:.1f}% ± {r['SMAPE_std']:.1f}%\", axis=1)\n"
