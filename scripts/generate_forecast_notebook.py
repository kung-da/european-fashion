        "\n"
        "### Target & Horizon\n"
        "- **Target**: `daily_quantity` = SUM(quantity) theo ngày\n"
        "- **Horizon**: 7 ngày\n"
        "\n"
        "### Granularity (2 tầng)\n"
        "| Tầng | Granularity | Số series | Data points/series |\n"
