        "        r['fold'] = fold_i + 1\n"
        "        results.append(r)\n"
        "\n"
        "        # MA-7: predict = rolling_mean_7\n"
        "        ma7_pred = df.loc[val_idx, 'rolling_mean_7'].values\n"
        "        r = evaluate(y_val, ma7_pred, f'{label_prefix}MA-7')\n"
        "        r['fold'] = fold_i + 1\n"
        "        results.append(r)\n"
        "\n"
        "        # Linear Regression\n"
        "        X_tr = df.loc[train_idx, feature_cols].values\n"
        "        y_tr = df.loc[train_idx, TARGET].values\n"
        "        X_va = df.loc[val_idx, feature_cols].values\n"
        "        lr = LinearRegression()\n"
        "        lr.fit(X_tr, y_tr)\n"
        "        lr_pred = np.clip(lr.predict(X_va), 0, None)  # quantity >= 0\n"
        "        r = evaluate(y_val, lr_pred, f'{label_prefix}LinReg')\n"
        "        r['fold'] = fold_i + 1\n"
        "        results.append(r)\n"
        "\n"
        "    # --- Final Test ---\n"
        "    y_test = df.loc[test_idx, TARGET].values\n"
        "\n"
        "    # Naive test\n"
        "    naive_test = df.loc[test_idx, 'lag_1'].values\n"
        "    r = evaluate(y_test, naive_test, f'{label_prefix}Naive')\n"
        "    r['fold'] = 'Test'\n"
        "    results.append(r)\n"
        "\n"
        "    # MA-7 test\n"
        "    ma7_test = df.loc[test_idx, 'rolling_mean_7'].values\n"
        "    r = evaluate(y_test, ma7_test, f'{label_prefix}MA-7')\n"
        "    r['fold'] = 'Test'\n"
        "    results.append(r)\n"
        "\n"
        "    # LinReg test\n"
        "    train_all_idx = df.index.difference(test_idx)\n"
        "    lr = LinearRegression()\n"
        "    lr.fit(df.loc[train_all_idx, feature_cols], df.loc[train_all_idx, TARGET])\n"
        "    lr_test_pred = np.clip(lr.predict(df.loc[test_idx, feature_cols]), 0, None)\n"
        "    r = evaluate(y_test, lr_test_pred, f'{label_prefix}LinReg')\n"
        "    r['fold'] = 'Test'\n"
        "    results.append(r)\n"
        "\n"
        "    return results\n"
        "\n"
        "\n"
